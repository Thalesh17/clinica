    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $filter = $request->input('filter');
        $permissoes = Permission::orderBy('name');

        if($filter)
        {
            $permissoes->where("name", "ilike", "%$filter%")
                        ->orWhere("description", "ilike", "%$filter%");
        }

        $permissoes = $permissoes->paginate(10)->appends('filter', request('filter'));

        return view('pages.permissao.index', compact('permissoes'));
    }

